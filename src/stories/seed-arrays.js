export const generateName = () => {
  const consonants = "bcdfghjklmnprstvwxz";
  const vowels = "aeiouy";

  let nameLength = Math.round(Math.random() * 5) + 5;
  let name = "";

  for (let i = 0; i < nameLength; i++) {
    const sampleCons = i % 2 === 0;
    const sampleLength = sampleCons ? consonants.length : vowels.length;
    const sample = Math.floor(Math.random() * sampleLength);

    name += sampleCons ? consonants[sample] : vowels[sample];

    if (i === 0) {
      name = name.toUpperCase();
    }
  }

  return name;
};

export const generateImage = () => {
  return testImages[Math.floor(Math.random() * testImages.length)];
};

export const generateTime = () => {
  let date = new Date();
  const day = Math.round(Math.random() * 29) + 1;
  const month = Math.round(Math.random() * 5) + 1;
  date.setFullYear(2020, month, day);

  const hour = Math.round(Math.random() * 24);
  const min = Math.round(Math.random() * 60);
  const sec = Math.round(Math.random() * 60);
  date.setHours(hour, min, sec);

  return date;
};

export const generateMessage = () => {
  const numWords = Math.round(Math.random() * 50) + 1;

  let message = "";
  for (let i = 0; i < numWords; i++) {
    message = `${message} ${generateName().toLowerCase()}`;
  }

  return message;
};

export const generateTestUsers = (numUsers = 100) => {
  let testUsers = [];

  for (let i = 0; i < numUsers; i++) {
    testUsers.push({
      id: i + 1,
      username: generateName(),
      firstName: generateName(),
      lastName: generateName(),
      avatar: generateImage()
    });
  }

  return testUsers;
};

export const generateTestMessages = (numMessages = 100) => {
  let testMessages = [];

  for (let i = 0; i < numMessages; i++) {
    const testUser = generateTestUsers(1)[0];

    testMessages.push({
      author: {
        avatar: testUser.avatar,
        id: testUser.id,
        username: testUser.username
      },
      channelId: 1,
      content: generateMessage(),
      createdAt: generateTime(),
      id: i + 1,
      me: testUser.username,
      upload: null,
      userid: testUser.id
    });
  }

  return testMessages;
};

export const generateTestRooms = (numRooms = 100, skipMe = false) => {
  let testRooms = [];

  const watching = Math.round(Math.random() * numRooms);

  for (let i = 0; i < numRooms; i++) {
    const numMembers =
      i === 0 && !skipMe ? 1 : Math.round(Math.random() * 6) + 1;

    let members = [];
    for (let j = 0; j < numMembers; j++) {
      members.push({
        id: j + 1,
        avatar: generateImage(),
        username: generateName()
      });
    }

    testRooms.push({
      id: i + 1,
      members: members,
      type:
        i === 0 ? "self" : Math.round(Math.random()) === 1 ? "friend" : "group",
      online: Math.round(Math.random()) === 1,
      watching: watching === i,
      notifications:
        Math.round(Math.random()) === 0 ? null : Math.round(Math.random() * 50),
      lastMessageContent: generateMessage(),
      lastMessageAt: generateTime().toDateString()
    });
  }

  return testRooms;
};
